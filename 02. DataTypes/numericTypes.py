# Integer
a = 13
b = 100
c = -66
print(a, b, c)
print(type(a))

# Float
x = 33.5
y = -25.8
z = 205.0
print(x, y, z)
print(type(z))

# Complex
d = 3+5j
print(d)
print(type(d))

# Binary
e = 0B1010
print(e)
print(type(e))

# Hexadecimal
f = 0XFF
print(f)
print(type(f))

# Boolean
g = True
print(g)
print(type(g))
print(9 > 8)
print(9 < 8)

# Conversion to int
h = int(x)
print(h)
print(type(h))

# Conversion to float
i = float('22.5')
print(i)
print(type(i))

# Conversion to binary
print(bin(10))

# Conversion to hexadecimal
print(hex(10))

# Conversion to octal
print(oct(10))
