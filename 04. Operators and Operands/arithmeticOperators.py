a, b = 10, 5

# Addition
print('Addition: ', a + b)

# Subtraction
print('Subtraction: ', a - b)

# Multiplication
print('Multiplication: ', a * b)

# Division
print('Division: ', a / b)

# Modulus
print('Modulus: ', a % b)

# Power
print('Power: ', a ** b)

# Floor division
print('Floor division: ', a // b)
